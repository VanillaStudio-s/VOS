void screen_init(uint64_t fb_addr) {
    framebuffer = (uint32_t*)fb_addr;

    if (!framebuffer) {
        volatile uint16_t* vga = (volatile uint16_t*)0xB8000;
        const char* msg = "SCREEN ERROR: framebuffer addr is NULL";
        for (int i = 0; msg[i]; i++)
            vga[i] = (uint16_t)(0x4F00 | (unsigned char)msg[i]);
        return;
    }

    uint32_t* p = back_buffer;
    for (int i = 0; i < SCREEN_WIDTH * SCREEN_HEIGHT; i++) *p++ = 0;
}

void put_pixel(int x, int y, uint32_t color) {
    if ((unsigned)x >= SCREEN_WIDTH || (unsigned)y >= SCREEN_HEIGHT) return;
    if (x < _cx0 || x >= _cx1 || y < _cy0 || y >= _cy1) return;
    back_buffer[y * SCREEN_WIDTH + x] = color;
}

uint32_t get_pixel(int x, int y) {
    if ((unsigned)x >= SCREEN_WIDTH || (unsigned)y >= SCREEN_HEIGHT) return 0;
    return back_buffer[y * SCREEN_WIDTH + x];
}

void screen_flip(void) {
    if (!framebuffer) return;
    uint32_t* src = back_buffer;
    uint32_t* dst = framebuffer;
    for (int i = 0; i < SCREEN_WIDTH * SCREEN_HEIGHT; i++)
        dst[i] = src[i];
}

void clear_screen(uint32_t color) {
    uint32_t* p = back_buffer;
    for (int i = 0; i < SCREEN_WIDTH * SCREEN_HEIGHT; i++) *p++ = color;
}

void draw_rect(int x, int y, int w, int h, uint32_t color) {
    for (int dy = 0; dy < h; dy++)
        for (int dx = 0; dx < w; dx++)
            put_pixel(x+dx, y+dy, color);
}

void draw_rect_outline(int x, int y, int w, int h, uint32_t color) {
    for (int dx = 0; dx < w; dx++) {
        put_pixel(x+dx, y,     color);
        put_pixel(x+dx, y+h-1, color);
    }
    for (int dy = 0; dy < h; dy++) {
        put_pixel(x,     y+dy, color);
        put_pixel(x+w-1, y+dy, color);
    }
}

void draw_line(int x0, int y0, int x1, int y1, uint32_t color) {
    int dx = x1-x0, dy = y1-y0;
    int steps = dx<0?-dx:dx;
    if ((dy<0?-dy:dy) > steps) steps = dy<0?-dy:dy;
    if (!steps) { put_pixel(x0,y0,color); return; }
    int xi = dx*256/steps, yi = dy*256/steps;
    int x = x0*256, y = y0*256;
    for (int i = 0; i <= steps; i++) {
        put_pixel(x/256, y/256, color);
        x += xi; y += yi;
    }
}

void draw_char(int x, int y, char c, uint32_t color, uint32_t bg) {
    if ((unsigned char)c < 32 || (unsigned char)c > 127) c = '?';
    const unsigned char* g = font8x16[(unsigned char)c - 32];
    for (int row = 0; row < 16; row++)
        for (int col = 0; col < 8; col++)
            if ((g[row] >> col) & 1)
                put_pixel(x+col, y+row, color);
            else if (bg != COLOR_TRANSPARENT)
                put_pixel(x+col, y+row, bg);
}

void draw_string(int x, int y, const char* s, uint32_t color, uint32_t bg) {
    int cx = x;
    while (*s) {
        if (*s == '\n') { cx = x; y += 17; }
        else { draw_char(cx, y, *s, color, bg); cx += 8; }
        s++;
    }
}

/* ── Terminal ───────────────────────────────────────────────────────────────── */
static int term_x = 0, term_y = 0;
#define TERM_W 8
#define TERM_H 17

void my_clear_screen(void) {
    clear_screen(COLOR_BLACK);
    term_x = term_y = 0;
}

void my_putchar_color(char c, unsigned char ci) {
    uint32_t fg = term_palette[ci & 0x0F];
    if (c == '\n') {
        term_x = 0; term_y += TERM_H;
    } else if (c == '\b') {
        if (term_x >= TERM_W) {
            term_x -= TERM_W;
            draw_rect(term_x, term_y, TERM_W, TERM_H, COLOR_BLACK);
        }
    } else {
        draw_char(term_x, term_y, c, fg, COLOR_BLACK);
        term_x += TERM_W;
        if (term_x >= SCREEN_WIDTH) { term_x = 0; term_y += TERM_H; }
    }
    if (term_y + TERM_H > SCREEN_HEIGHT) {
        int rp = SCREEN_WIDTH * TERM_H;
        int tot = SCREEN_WIDTH * SCREEN_HEIGHT;
        for (int i = 0; i < tot - rp; i++) back_buffer[i] = back_buffer[i + rp];
        for (int i = tot-rp; i < tot; i++) back_buffer[i] = 0;
        term_y -= TERM_H;
    }
}

void my_putchar(char c)  { my_putchar_color(c, 15); }
void my_puts(const char* s) { while(*s) my_putchar_color(*s++, 15); }
void my_puts_color(const char* s, unsigned char ci) { while(*s) my_putchar_color(*s++, ci); }

void my_print_int(int n) {
    if (n < 0) { my_putchar('-'); n = -n; }
    if (n == 0) { my_putchar('0'); return; }
    char buf[12]; int i = 0;
    while (n > 0) { buf[i++] = (n%10)+'0'; n /= 10; }
    while (i > 0) my_putchar(buf[--i]);
}

int my_strcmp(const char* s1, const char* s2) {
    while (*s1 && (*s1 == *s2)) { s1++; s2++; }
    return *(const unsigned char*)s1 - *(const unsigned char*)s2;
}

